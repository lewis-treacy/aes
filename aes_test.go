/* UTILS */
func compareBytes(a, b []byte) (bool, error) {
	if len(a) != len(b) {
		return false, errors.New("lengths differ")
	}
	for i, v := range a {
		if v != b[i] {
			return false, errors.New(fmt.Sprintf("index %d, 0x%02X != 0x%02X", i, v, b[i]))
		}
	}
	return true, nil
}
